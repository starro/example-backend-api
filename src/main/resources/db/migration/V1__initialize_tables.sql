CREATE TABLE SAMPLE_POST (
     POST_NO     NUMBER(20) NOT NULL AUTO_INCREMENT(1001)
    ,USER_ID     VARCHAR2(50  BYTE) 
    ,TITLE       VARCHAR2(200 BYTE) 
    ,CONTENT     CLOB 
    ,CREATED_AT  TIMESTAMP(6) 
    ,UPDATED_AT  TIMESTAMP(6) 
    ,PRIMARY KEY(POST_NO)
);

CREATE TABLE SAMPLE_COMMENT (
     COMMENT_NO  NUMBER(20) NOT NULL AUTO_INCREMENT(1001)
    ,POST_NO     NUMBER(20) NOT NULL 
    ,CONTENT     CLOB 
    ,CREATED_AT  TIMESTAMP(6) 
    ,UPDATED_AT  TIMESTAMP(6)
    ,PRIMARY KEY(COMMENT_NO) 
);